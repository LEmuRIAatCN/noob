public class NoobClusterInvoker<T> implements Invoker<T> {

    private static final Logger logger = LoggerFactory.getLogger(NoobClusterInvoker.class);

    private final Directory<T> directory;

    private final Invoker<T> invoker;

    public NoobClusterInvoker(Directory<T> directory, Invoker<T> invoker) {
        this.directory = directory;
        this.invoker = invoker;
    }

    @Override
    public Class<T> getInterface() {
        return null;
    }

    @Override
    public Result invoke(Invocation invocation) throws RpcException {
        return directory.list(invocation).get(0).invoke(invocation);
    }

    @Override
    public URL getUrl() {
        return null;
    }

    @Override
    public boolean isAvailable() {
        return false;
    }

    @Override
    public void destroy() {

    }
}
